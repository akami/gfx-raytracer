package at.ac.univie.unet.a01638800.raytracer.io.wavefront;

public class WavefrontParserException extends Exception {

    public WavefrontParserException() {
    }

    public WavefrontParserException(final String message) {
        super(message);
    }

    public WavefrontParserException(final String message, final Throwable cause) {
        super(message, cause);
    }

    public WavefrontParserException(final Throwable cause) {
        super(cause);
    }

    public WavefrontParserException(final String message, final Throwable cause, final boolean enableSuppression, final boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

}
